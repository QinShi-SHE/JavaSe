public class Method_01 {
    public static void main(String[] args) {
        /*
             方法特点：
                    可以独立完成某一个功能
                    可以重复使用
                    每一次使用后，产生的结果是独立的，互不干扰
                    存在与类的结构体中，在类的大括号里面
               作用：方便使用

               public static void main(String[] args){
                }
                public：修饰符 表示公共的
                static：修饰符 jvm优先加载
                void  ：此处为返回值类型，void表示方法没有返回值
                main  : 方法名字
                ()    : 圆括号  固定格式用来存放形式参数
                String[] args:形式参数  String[]参数类型 args参数名字
                {}    :方法结构体  里面存放需要方法完成的功能语句

         */
    }
}
