public class ConstantTaype {
    public static void main(String[] args) {
        //常量，不能改变的值
        System.out.println(1); //整型常量
        System.out.println("琴");//字符串常量
        System.out.println('a');//字符常量
        System.out.println(3.33);//浮点常量
        System.out.println(true);//布尔常量
    }
}
